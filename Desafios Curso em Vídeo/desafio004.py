a = input('Digite algo: ')
print('O tipo primitivo desse valor é ', type(a))
print('É inteiramente númerico? ', a.isnumeric())
print('É inteiramente alfabético? ', a.isalpha())
print('É interamente em minusculo? ', a.islower())
print('É interiamente em maiusculo? ', a.isupper())
print('Só tem espaços? ', a.isspace())
print('Está captalizada? ', a.istitle())








